// The function now accepts a lifetime parameter 'a to indicate that
// the returned reference will live at least as long as the input references.
fn longest<'a>(x: &'a str, y: &'a str) -> &'a str {
    if x.len() > y.len() {
        x
    } else {
        y
    }
}

